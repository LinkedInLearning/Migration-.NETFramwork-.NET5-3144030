CREATE TABLE [Departments]
(
    [DepartmentId] INT CONSTRAINT PK_Department_Id PRIMARY KEY,
    [Name] NVARCHAR(64),
    [Place] NVARCHAR(64) 
);

GO

CREATE TABLE [Employees]
(
    [EmployeeId] INT CONSTRAINT PK_Employee_Id PRIMARY KEY,
    [Name] NVARCHAR(64),
    [Job] NVARCHAR(64),
    [ManagerId] INT,
    [HireDate] DATETIME,
    [Salary] NUMERIC,
    [Department_DepartmentId] INT CONSTRAINT FK_Department_Id REFERENCES [Departments]([DepartmentId])
);

